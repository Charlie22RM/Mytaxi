public class ViajeBL {
    
    String id_viaje;
    String numero_pasajeros;
    String id_ruta;
    String placa_taxi;

    public String getId_viaje() {
        return id_viaje;
    }

    public void setId_viaje(String id_viaje) {
        this.id_viaje = id_viaje;
    }

    public String getNumero_pasajeros() {
        return numero_pasajeros;
    }

    public void setNumero_pasajeros(String numero_pasajeros) {
        this.numero_pasajeros = numero_pasajeros;
    }

    public String getId_ruta() {
        return id_ruta;
    }

    public void setId_ruta(String id_ruta) {
        this.id_ruta = id_ruta;
    }

    public String getPlaca_taxi() {
        return placa_taxi;
    }

    public void setPlaca_taxi(String placa_taxi) {
        this.placa_taxi = placa_taxi;
    }
    
}
